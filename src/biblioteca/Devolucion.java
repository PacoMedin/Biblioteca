    public void BusquedaLibro() {
        Conexion c = new Conexion();
        PreparedStatement ps = null;
        ResultSet rs = null;

        try {
            int cod = Integer.parseInt(codPrestamo.getText());
            String queryG = "SELECT Titulo,d.Id_Usuario,d.Estado from libro l\n"
                    + "join detalles d on d.cod_Libro=l.cod_Libro\n"
                    + "where cod_Prestamo=" + cod;

            Connection con = c.getConexion();
            ps = con.prepareStatement(queryG);
            rs = ps.executeQuery();

            ResultSetMetaData rsmd;

            rsmd = rs.getMetaData();
            int cantColumnas = rsmd.getColumnCount();
            Object[] filas = new Object[cantColumnas];
            while (rs.next()) {

                for (int i = 0; i < cantColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }//for

            }//while

            titulo.setText(filas[0] + "");
            id.setText(filas[1] + "");
            combo.setSelectedItem(filas[3]);

        } catch (Exception ex) {
            System.out.println(ex);
        }
    }

